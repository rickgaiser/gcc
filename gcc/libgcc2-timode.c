#ifdef L_cmpti2
word_type
__cmpti2 (TItype a, TItype b)
{
  TIunion au, bu;

  au.ti = a, bu.ti = b;

  if (au.d.high < bu.d.high)
    return 0;
  else if (au.d.high > bu.d.high)
    return 2;
  if ((UDItype) au.d.low < (UDItype) bu.d.low)
    return 0;
  else if ((UDItype) au.d.low > (UDItype) bu.d.low)
    return 2;
  return 1;
}
#endif

